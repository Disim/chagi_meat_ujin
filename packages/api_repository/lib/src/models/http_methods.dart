/// Перечисление HttpMethods содержит возможные HTTP методы запросов.
enum HttpMethods {
  /// Метод GET для получения данных.
  GET,

  /// Метод POST для отправки данных.
  POST,

  /// Метод DELETE для удаления данных.
  DELETE,

  /// Метод PUT для обновления данных.
  PUT,

  /// Метод PATCH для частичного обновления данных.
  PATCH,
}
